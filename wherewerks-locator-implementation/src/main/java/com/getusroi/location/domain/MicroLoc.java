package com.getusroi.location.domain;

public class MicroLoc {

	private String locId;
	private String descId;
	private String attributeId;
	private String dimId;
	private String locCode;
	private String addressId;
	private String locAccessId;
	private String locAuthId;
	private String attributeGroup;
	private String containerId;
	private String mapId;
	private String locStatus;

	public String getLocId() {
		return locId;
	}

	public void setLocId(String locId) {
		this.locId = locId;
	}

	public String getDescId() {
		return descId;
	}

	public void setDescId(String descId) {
		this.descId = descId;
	}

	public String getAttributeId() {
		return attributeId;
	}

	public void setAttributeId(String attributeId) {
		this.attributeId = attributeId;
	}

	public String getDimId() {
		return dimId;
	}

	public void setDimId(String dimId) {
		this.dimId = dimId;
	}

	public String getLocCode() {
		return locCode;
	}

	public void setLocCode(String locCode) {
		this.locCode = locCode;
	}

	public String getAddressId() {
		return addressId;
	}

	public void setAddressId(String addressId) {
		this.addressId = addressId;
	}

	public String getLocAccessId() {
		return locAccessId;
	}

	public void setLocAccessId(String locAccessId) {
		this.locAccessId = locAccessId;
	}

	public String getLocAuthId() {
		return locAuthId;
	}

	public void setLocAuthId(String locAuthId) {
		this.locAuthId = locAuthId;
	}

	public String getAttributeGroup() {
		return attributeGroup;
	}

	public void setAttributeGroup(String attributeGroup) {
		this.attributeGroup = attributeGroup;
	}

	public String getContainerId() {
		return containerId;
	}

	public void setContainerId(String containerId) {
		this.containerId = containerId;
	}

	public String getMapId() {
		return mapId;
	}

	public void setMapId(String mapId) {
		this.mapId = mapId;
	}

	public String getLocStatus() {
		return locStatus;
	}

	public void setLocStatus(String locStatus) {
		this.locStatus = locStatus;
	}

}
